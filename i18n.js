let currentLang = "pt";

function t(key) {
  const keys = key.split(".");
  let val = translations[currentLang];
  for (const k of keys) {
    if (val && val[k] !== undefined) val = val[k];
    else return key;
  }
  return val;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("rfm_lang", lang);
}

function getLang() {
  return currentLang;
}

function initLang() {
  const saved = localStorage.getItem("rfm_lang");
  if (saved && translations[saved]) {
    currentLang = saved;
  }
}
